#include <bits/stdc++.h>

using namespace std;

int main()
{
    float a = 1.5;
    cout << (int)a << endl;

    float b = 1.4;
    cout << (int)b << endl;

    float c = -1.5;
    cout << (int)c << endl;

    float d = -1.4;
    cout << (int)d << endl;

    float e = 1.6;
    cout << (int)e << endl;

    float f = -1.6;
    cout << (int)f << endl;

    return 0;
}